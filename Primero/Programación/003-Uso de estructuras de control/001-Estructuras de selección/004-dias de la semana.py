dia = "miercoles"

if dia == "lunes":
    print("Hoy es el peor día de la semana")
elif dia == "martes":
    print("Hoy es segundo peor día de la semana")
elif dia  == "miercoles":
    print("¡Que nos quedan solo dos dias para Viernes!")
elif dia == "jueves":
    print("Hoy es Juernes")
elif dia == "viernes":
    print("Por fin es Viernes, let's go")
elif dia == "sabado":
    print("Hoy es el mejor día de la semana")
elif dia == "domingo":
    print("No me puedo creer que mañana ya sea Lunes")
else:
    print("Lo que has escrito no es un día")
